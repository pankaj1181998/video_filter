import os
import matplotlib.pyplot as plt
import matplotlib.image as mpimg
from tensorflow.keras import layers
from tensorflow.keras import Model
from tensorflow.keras.optimizers import RMSprop
from tensorflow.keras.preprocessing.image import ImageDataGenerator
import numpy as np
import random
from tensorflow.keras.preprocessing.image import img_to_array, load_img


'''
    Creating Directiories variables -----------------------------------------
'''
base_dir = 'D:/Video_filtering/data'
train_dir = os.path.join(base_dir, 'train')
validation_dir = os.path.join(base_dir, 'validation')

# Directory with our training pictures nsfw
nsfw_dir = os.path.join(train_dir, 'nsfw')
    
# Directory with our training dog pictures
sfw_dir = os.path.join(train_dir, 'sfw')


# Directory with our validation pictures sfw
validation_nsfw_dir = os.path.join(validation_dir, 'nsfw')

# Directory with our validation pictures sfw
validation_sfw_dir = os.path.join(validation_dir, 'sfw')

